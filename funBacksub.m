function [x] = funBacksub(U, z)
[m,~] = size(U);
x(m) = z(m);
for ii=m-1:-1:1
    t = 0;
    for jj=ii+1:m
        t = t + U(ii, jj)*x(jj);
    end
    x(ii) = z(ii)-t;
end
end