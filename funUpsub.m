function [z] = funUpsub(L, b)
    [m,~] = size(L);
    z = b;
    z(1) = b(1)/L(1,1);
    for ii=2:m
        t = 0;
        for jj=1:ii-1
            t = t + L(ii, jj)*z(jj);
        end
        z(ii) = (b(ii)-t)/L(ii, ii);
    end
end